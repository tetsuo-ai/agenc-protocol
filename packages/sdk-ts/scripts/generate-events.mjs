/**
 * Renders src/generated/events/ from the IDL and appends the events barrel
 * export to the Codama-written src/generated/index.ts (idempotent).
 */
export async function generateEvents() {
  const idl = JSON.parse(readFileSync(IDL_PATH, "utf8"));
  const typesByName = new Map((idl.types ?? []).map((t) => [t.name, t]));

  const events = [...(idl.events ?? [])]
    .sort((a, b) => (a.name < b.name ? -1 : a.name > b.name ? 1 : 0))
    .map((event) => {
      const struct = typesByName.get(event.name);
      if (!struct || struct.type?.kind !== "struct") {
        throw new Error(`IDL event ${event.name} has no matching struct in idl.types`);
      }
      if (!Array.isArray(event.discriminator) || event.discriminator.length !== 8) {
        throw new Error(`IDL event ${event.name} has no 8-byte discriminator`);
      }
      return {
        name: event.name,
        discriminator: event.discriminator,
        docs: struct.docs ?? [],
        fields: struct.type.fields ?? [],
      };
    });

  // Duplicate discriminators would make the lookup table silently drop events.
  const seenDiscriminators = new Map();
  for (const e of events) {
    const hex = discriminatorHex(e.discriminator);
    if (seenDiscriminators.has(hex)) {
      throw new Error(
        `Duplicate event discriminator ${hex}: ${seenDiscriminators.get(hex)} vs ${e.name}`,
      );
    }
    seenDiscriminators.set(hex, e.name);
  }

  rmSync(EVENTS_OUT, { recursive: true, force: true });
  mkdirSync(EVENTS_OUT, { recursive: true });
  for (const event of events) {
    writeFileSync(
      path.join(EVENTS_OUT, `${camelCase(event.name)}.ts`),
      renderEventFile(event, typesByName),
    );
  }
  writeFileSync(path.join(EVENTS_OUT, "agencEvent.ts"), renderUnionFile(events));
  writeFileSync(path.join(EVENTS_OUT, "index.ts"), renderBarrel(events));

  // Append the events export to the Codama barrel (idempotent; generate.mjs
  // rewrites the barrel each run, so this re-appends after every regen).
  const barrelPath = path.join(OUT, "index.ts");
  const exportLine = 'export * from "./events/index.js";';
  let barrel = readFileSync(barrelPath, "utf8");
  if (!barrel.includes(exportLine)) {
    if (!barrel.endsWith("\n")) barrel += "\n";
    writeFileSync(barrelPath, barrel + exportLine + "\n");
  }

  console.log(`OK — generated ${events.length} event codecs into src/generated/events/`);
}

const isMain =
  process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) {
  await generateEvents();
}
